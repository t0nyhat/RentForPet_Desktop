namespace PetHotel.Domain.Enums;

/// <summary>
/// Режим расчета бронирований.
/// </summary>
public enum BookingCalculationMode
{
    /// <summary>
    /// Расчет по дням (текущий режим)
    /// - Оплачивается день заезда и день выезда
    /// - Формула: (CheckOut - CheckIn).Days + 1
    /// - День заезда и выезда полностью блокируется для других бронирований.
    /// </summary>
    Days = 0,

    /// <summary>
    /// Расчет по ночам (режим гостиниц)
    /// - Оплата по количеству ночей
    /// - Формула: (CheckOut - CheckIn).Days
    /// - Заезд после указанного времени (по умолчанию 15:00)
    /// - Выезд до указанного времени (по умолчанию 12:00)
    /// - День заезда и выезда доступен для других бронирований.
    /// </summary>
    Nights = 1
}
